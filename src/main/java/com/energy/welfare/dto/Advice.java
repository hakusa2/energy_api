package com.energy.welfare.dto;

public class Advice {

    private String phone;
    private String email;

    public Advice() {
    }

    public String getPhone() {
        return phone;
    }
    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
